CREATE TABLE IF NOT EXISTS roles (
    rol_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    rol_name VARCHAR(50) NOT NULL UNIQUE
);

CREATE TABLE IF NOT EXISTS users (
    use_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    use_name VARCHAR(255) NOT NULL UNIQUE,
    use_password TEXT NOT NULL,
    use_email VARCHAR(255) NOT NULL UNIQUE,
    use_created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    use_updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS users_roles (
    use_id INTEGER NOT NULL,
    rol_id INTEGER NOT NULL,
    PRIMARY KEY (use_id, rol_id),
    CONSTRAINT fk_user FOREIGN KEY (use_id) REFERENCES users (use_id) ON DELETE CASCADE,
    CONSTRAINT fk_role FOREIGN KEY (rol_id) REFERENCES roles (rol_id) ON DELETE CASCADE
);

CREATE INDEX IF NOT EXISTS idx_users_use_name ON users (use_name);
CREATE INDEX IF NOT EXISTS idx_users_use_email ON users (use_email);

